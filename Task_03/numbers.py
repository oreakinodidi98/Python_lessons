# ask the user to enter 3 different integers
print("Enter 3 different numbers:")
num1 = int(input("Enter the first number:"))
num2 = int(input("Enter the second number:"))
num3 = int(input("Enter the third number:"))
# print out sum of all 3 numbers
print("The sum of the numbers is:", num1 + num2 + num3)
# print out sum of first number minus second number
print("The sum of the first number minus the second number is:", num1 - num2)
# print out sum of thrid number multiplied by first number
print("The sum of the third number multiplied by the first number is:", num3 * num1)
# print out sum of all three numbers divided by the thrid number
print("The sum of all three numbers divided by the third number is:", (num1 + num2 + num3) / num3)